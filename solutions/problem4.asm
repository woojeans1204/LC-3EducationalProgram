; 문제 4: R0의 절댓값을 계산하여 R0에 저장하는 프로그램
; 목표: 조건부 분기와 부호 비트를 활용하여 절댓값을 구하는 방법을 학습합니다.

        .ORIG x3000          ; 코드 섹션 시작 주소

START   ADD R0 R0 #0
        BRzp END             ; R0 >= 0이면 END로 분기
        NOT R0, R0           ; R0 = ~R0
        ADD R0, R0, #1       ; R0 = -R0 (2의 보수)
END     HALT                 ; 프로그램 종료

        .END                 ; 프로그램 끝
